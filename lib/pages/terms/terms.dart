  Widget ruleItem(String title, String rule, bool ex, int ind) {
    if(rule.length <= 100){
      ex = true;
    }
    return SizedBox(
      child: Card(
        color: Colors.white,
        child: Column(
          children: [
            Text(
              title,
              style: Theme.of(context).textTheme.titleLarge,
            ),
            const SizedBox(
              height: 5.0,
            ),
            Padding(
              padding: const EdgeInsets.all(15.0),
              child: Text(
                ex ? rule : "${rule.substring(0, 800)}... ادامه",
                style: Theme.of(context).textTheme.bodyMedium, textAlign: TextAlign.justify,
                maxLines: !ex ? 12 : null,
              ),
            ),
            !ex
                ? IconButton(
                    onPressed: () {
                      setState(() {
                        exp[ind] = !exp[ind];
                      });
                    },
                    icon: const Icon(
                      Icons.expand_circle_down,
                      color: Colors.green,
                    ),
                  )
                : Container()
          ],
        ),
      ),
